async function updateRegistry({ githubRepo, githubToken, registryItem }) {
  const path = 'apps/registry.json';
  const existing = await getGitHubFile(githubRepo, path, githubToken);
  let registry = { apps: [], updatedAt: new Date().toISOString(), source: 'logichub-publish' };
  if (existing?.content) {
    try {
      registry = JSON.parse(fromBase64(existing.content.replace(/\n/g, '')));
    } catch (error) {
      registry = { apps: [], updatedAt: new Date().toISOString(), source: 'logichub-publish' };
    }
  }

  const apps = Array.isArray(registry.apps) ? registry.apps : [];
  const withoutCurrent = apps.filter((app) => app.slug !== registryItem.slug);
  const next = {
    apps: [registryItem, ...withoutCurrent].slice(0, 300),
    updatedAt: new Date().toISOString(),
    source: 'logichub-publish'
  };

  await putGitHubFile(
    githubRepo,
    path,
    githubToken,
    `LogicHub Marketplace: register ${registryItem.name}`,
    JSON.stringify(next, null, 2),
    existing?.sha
  );
}

export default async function handler(req, res) {
  if (req.method !== 'POST') return res.status(405).json({ error: 'Method not allowed. Use POST.' });

  const appName = String(req.body?.appName || '').trim();
  const bundle = req.body?.bundle;
  const incomingMetadata = req.body?.metadata || {};

  if (!appName || !bundle) return res.status(400).json({ error: 'Missing appName or bundle.' });

  try {
    validateBundle(bundle);

    const slug = normalizeSlug(req.body?.slug || incomingMetadata.slug || appName);
    const githubRepo = 'via-decide/daxini.space';
    const githubToken = process.env.GITHUB_TOKEN;
    const files = bundle.app;

    const metadata = {
      name: appName,
      creator: String(incomingMetadata.creator || 'username'),
      created_from: 'logichub',
      category: String(incomingMetadata.category || 'tool'),
      version: String(incomingMetadata.version || '1.0'),
      slug,
      description: String(incomingMetadata.description || `${appName} published from LogicHub`),
      url: `https://daxini.space/app/${slug}`,
      appUrl: `https://daxini.space/apps/${slug}/app/index.html`,
      previewImage: String(incomingMetadata.previewImage || 'https://daxini.space/assets/default-app-preview.png'),
      isPublished: true,
      publishedAt: new Date().toISOString(),
      source: 'logichub'
    };

    const fileEntries = [
      ['apps/' + slug + '/app/index.html', files['index.html']],
      ['apps/' + slug + '/app/app.js', files['app.js']],
      ['apps/' + slug + '/app/style.css', files['style.css']],
      ['apps/' + slug + '/app/manifest.json', files['manifest.json']],
      ['apps/' + slug + '/app/sw.js', files['sw.js']],
      ['apps/' + slug + '/app/metadata.json', JSON.stringify(metadata, null, 2)],
      ['apps/' + slug + '/architecture.json', JSON.stringify({ prd: bundle.architecture_prd || '' }, null, 2)],
      ['app/' + slug + '/index.html', buildAppPage(metadata)]
    ];

    for (const [path, content] of fileEntries) {
      const existing = await getGitHubFile(githubRepo, path, githubToken);
      await putGitHubFile(githubRepo, path, githubToken, `LogicHub Publish: ${appName} (${slug})`, String(content || ''), existing?.sha);
    }

    await updateRegistry({
      githubRepo,
      githubToken,
      registryItem: {
        slug,
        name: metadata.name,
        creator: metadata.creator,
        previewImage: metadata.previewImage,
        openApp: metadata.url,
        viewCode: `https://github.com/via-decide/daxini.space/tree/main/apps/${slug}`,
        appUrl: metadata.appUrl,
        source: 'logichub',
        publishedAt: metadata.publishedAt
      }
    });

    return res.status(200).json({
      success: true,
      slug,
      url: metadata.url,
      appUrl: metadata.appUrl,
      marketplace: 'https://daxini.space'
    });
  } catch (error) {
    console.error('Publish endpoint error:', error);
    return res.status(500).json({ error: `Publish failed: ${error.message}` });
  }
}
